package com.example.studyapp;

public class GroupVO {
    private String groupName;
    private String groupInfo;
    private String groupKind;
    private String groupPersonnel;

    public String getGroupName() {
        return groupName;
    }

    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }

    public String getGroupInfo() {
        return groupInfo;
    }

    public void setGroupInfo(String groupInfo) {
        this.groupInfo = groupInfo;
    }

    public String getGroupKind() {
        return groupKind;
    }

    public void setGroupKind(String groupKind) {
        this.groupKind = groupKind;
    }

    public String getGroupPersonnel() {
        return groupPersonnel;
    }

    public void setGroupPersonnel(String groupPersonnel) {
        this.groupPersonnel = groupPersonnel;
    }
}
